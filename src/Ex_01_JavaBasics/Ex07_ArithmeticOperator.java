package Ex_01_JavaBasics;

public class Ex07_ArithmeticOperator {
    public static void main(String[] args) {
        // Using Differnet operator in one class

// Arithmetic operator ( + , -, *, /, %
    // + (Addition), - (Subtraction), * (Multiplication), / (Division), % (Module) - Reminder

        int a = 10;
        long b = 10;
        float c = 3.123F;
        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);
        System.out.println(a % c);

        // Printing the value with PrintF
        System.out.printf("Another way to print value %d + %d = %d%n", a,b, (a+b));
        System.out.printf("Addition %d%n", (a + b));



    }
}
