package Ex_01_JavaBasics;

public class Ex08_AssignmentOperator {
    public static void main(String[] args) {
        // Assignment Operator
  // = (Assign), += (Add and Assign), -= (Subtract and assign),
  // *= (multiply and assign), /= (Division and assign), %= (Module and assign)

        int a = 10;
        int b = 20;
        float c = 3.14f;
        System.out.println(a += b);
        System.out.println(a -= b);
        System.out.println(a *= b);
        System.out.println(a /= b);
        System.out.println(a %= b);

    }
}
