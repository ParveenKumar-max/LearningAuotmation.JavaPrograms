package Ex_01_JavaBasics;

public class Ex04_PrntF {

    public static void main(String[] args) {
  // use of printF in java
        int number = 42;
        double pi = 3.14159;
        char character = 'A';

         // Using printf in Java
        System.out.printf("The number is: %d%n", number);
        System.out.printf("Value of pi: %.2f%n", pi); // %.2f formats to 2 decimal places
        System.out.printf("The character is: %c%n", character);
    }

}
